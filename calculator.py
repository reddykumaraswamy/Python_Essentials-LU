# simple calculator with addition, subtraction, multiplication and division functions in it.

a = int(input("Enter your First Number: "))
b = int(input("Enter your Second Number: "))

Sum = a + b
Sub = a - b
Mul = a * b
Div1 = a / b
Div2 = a // b

print("Addition: ", Sum)
print("Subtraction: ", Sub)
print("Multiplication: ", Mul)
print("Floating point division: ", Div1)
print("Integer division: ", Div2)